package Day_2;

public class TypeCastingDemo {

	public static void main(String[] args) {
		byte num1=78;
		byte num2=50;
		
		byte sum=(byte)(num1+num2);
		
		short s1=78;
		short s2=56;
		
		short r=(short)(s1+s2);
		System.out.println(r);
		System.out.println(sum);
		
		float n1=(float)78.5;
		float n2=67.3f;
		
		int result=(int)(n1+n2);
		System.out.println(result);
		
		int a=90;
		int b=45;
		
		float ans=a+b;
		System.out.println(ans);
		
		

	}

}
